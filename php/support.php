			<h2>Why should you donate ?</h2>
					<p>Jappix’s goal is building a new social platform, a social web that allows you to keep the control of your data and that gives you the freedom you need to communicate.</p>
					<p>We’re a small team of developers working on Jappix and we must have support do keep on a free software and the service that’s provided. If you gave money to the project, we could maintain a good service and continue our activities, serving communities, thousand of users that need a platform to communicate free.</p><p style="height:20px;"></p>
			<h2>What would your money be used for ?</h2>
				<p>When somebody helps us, the money he gives would be used mostly in the servers, to provide the best service we can. An other part is used for the communication.</p>
				<p>Because Jappix is lead by <a href="http://www.post-pro.fr">PostPro non profit organization</a>, Jappix founders aren’t, in any case, payed. All of the people working on Jappix are volunteer and don’t receive money for the work they do. Helping us by giving money is therefore allowing people to keep using Jappix as much as they want.</p><p style="clear:both; height:20px;"></p>
