<p>Lots of people feel frustrated of the available website mini-chats. This is the reason why we made Jappix Mini.</p>

<p>It is really simple to setup and to use. Besides, it was designed to be easy to integrate to every website system, such as Wordpress, SPIP, Dotclear, Drupal, phpBB or a home-made one.</p>

